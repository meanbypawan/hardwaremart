package com.hardwaremart.bean;

public class Product {
   private String id;
   private String name;
   private String description;
   private int price;
   public String getId(){
     return id;
   }
   public String getName(){
     return name;
   }
   public String getDescription(){
     return description;
   }
   public int getPrice(){
     return price;
   }
}
